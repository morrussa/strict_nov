-- 检查点是否在碰撞区域内
-- @param imageName: 图片文件名
-- @param x: 点的x坐标
-- @param y: 点的y坐标
-- @return: true如果在碰撞区域内，否则false
function M.check(imageName, x, y)
    local rects = collisions[imageName]
    if not rects then
        return false
    end

    for _, rect in ipairs(rects) do
        if x >= rect.x and x < rect.x + rect.width and
        y >= rect.y and y < rect.y + rect.height then
            return true
        end
    end

    return false
end

function M.get_rects(imageName)
    return collisions[imageName]
end

return M
